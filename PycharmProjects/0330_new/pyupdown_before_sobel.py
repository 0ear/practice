import cv2
img = cv2.imread('../img/1.jpg')
img = cv2.resize(img, (960,680))
a = cv2.Sobel(img, cv2.CV_16S,1,0,ksize = 1)
b = cv2.Sobel(img, cv2.CV_16S,1,0,ksize = 3)
c = cv2.Sobel(img, cv2.CV_16S,0,1,ksize = 3)
#cv2.imshow('a', a)
#cv2.imshow()
a1 = cv2.convertScaleAbs(a)
b1 = cv2.convertScaleAbs(b)
c1 = cv2.convertScaleAbs(c)
cv2.imshow('a1', a1)
cv2.imshow('b1', b1)
cv2.imshow('c1', c1)
cv2.waitKey()