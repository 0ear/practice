import cv2
img = cv2.imread('../img/1.jpg')
img = cv2.resize(img, (960,680))
a = cv2.Laplacian(img, cv2.CV_16S,ksize = 1)
b = cv2.Laplacian(img, cv2.CV_16S,ksize = 3)
c = cv2.Laplacian(img, cv2.CV_16S,ksize = 5)
d = cv2.Laplacian(img, cv2.CV_16S,ksize = 7)
#cv2.imshow('a', a)
#cv2.imshow()
a1 = cv2.convertScaleAbs(a)
b1 = cv2.convertScaleAbs(b)
c1 = cv2.convertScaleAbs(c)
d1 = cv2.convertScaleAbs(d)
cv2.imshow('a1', a1)
cv2.imshow('b1', b1)
cv2.imshow('c1', c1)
cv2.imshow('d1', d1)
cv2.waitKey()